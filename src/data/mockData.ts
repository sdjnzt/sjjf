// 模拟数据文件
export interface Device {
  id: string;
  name: string;
  type: 'server' | 'network' | 'sensor' | 'storage' | 'power' | 'cooling';
  status: 'online' | 'offline' | 'warning';
  location: string;
  lastUpdate: string;
  cpu?: number;
  memory?: number;
  totalMemory?: number; // 单位GB
  temperature?: number;
  power?: number;
  maxPower?: number; // 单位W
}

export interface OrganizationUnit {
  id: string;
  name: string;
  type: 'department' | 'team' | 'group';
  parentId?: string;
  manager: string;
  managerPhone?: string;
  memberCount: number;
  description?: string;
  location?: string;
  establishedDate?: string;
  budget?: number;
  children?: OrganizationUnit[];
}

export interface User {
  id: string;
  name: string;
  department: string;
  role: string;
  phone: string;
  email?: string;
  status: 'online' | 'offline' | 'busy';
  avatar?: string;
  joinDate?: string;
  supervisor?: string;
  level?: '初级' | '中级' | '高级' | '专家';
  workLocation?: string;
  employeeId?: string;
}

export interface Command {
  id: string;
  title: string;
  content: string;
  sender: string;
  receiver: string;
  status: 'pending' | 'sent' | 'received' | 'completed';
  timestamp: string;
  priority: 'low' | 'medium' | 'high' | 'urgent';
}

export interface SafetyEvent {
  id: string;
  type: 'fire' | 'temperature' | 'power' | 'intrusion' | 'water' | 'network';
  location: string;
  severity: 'low' | 'medium' | 'high' | 'critical';
  status: 'active' | 'resolved' | 'investigating';
  timestamp: string;
  description: string;
  responseTime?: string;
}

export interface DataRecord {
  id: string;
  deviceId: string;
  deviceName: string;
  dataType: 'temperature' | 'humidity' | 'cpu' | 'memory' | 'power' | 'voltage';
  value: number;
  unit: string;
  timestamp: string;
  location: string;
}

export interface InspectionRecord {
  id: string;
  title: string;
  type: 'routine' | 'special' | 'emergency' | 'annual';
  inspector: string;
  department: string;
  location: string;
  status: 'scheduled' | 'in_progress' | 'completed' | 'overdue';
  priority: 'low' | 'medium' | 'high' | 'urgent';
  scheduledDate: string;
  completedDate?: string;
  score?: number;
  issuesFound: number;
  rectificationItems: number;
  description: string;
  remarks?: string;
}

export interface RectificationItem {
  id: string;
  inspectionId: string;
  title: string;
  description: string;
  category: 'safety' | 'equipment' | 'process' | 'environment' | 'documentation';
  severity: 'low' | 'medium' | 'high' | 'critical';
  status: 'pending' | 'in_progress' | 'completed' | 'verified' | 'closed';
  assignee: string;
  department: string;
  dueDate: string;
  createdDate: string;
  completedDate?: string;
  verifiedDate?: string;
  progress: number;
  cost?: number;
  remarks?: string;
}

